    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String jmbg = txtJMBG.getText();
        String korIme = txtKorIme.getText();
        String lozinka = String.copyValueOf(txtPass.getPassword());
        String potvrda = String.copyValueOf(txtPotvrdaPass.getPassword());
        
        if (lozinka.matches(potvrda)) {
            Korisnik noviKor = new Korisnik(-1, ime, prezime, jmbg, korIme, lozinka);
            KontrolerKlijent.getInstanca().posaljiZahtevZaResitraciju(noviKor);
        } else {
            JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("forme/Bundle_sr_RS").getString("LOZINKE SE NE POKLAPAJU"));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

