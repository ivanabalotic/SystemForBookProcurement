    public void registrujKorisnika(ServerskiOdgovor so) {
        try {
            if (so.getIzuzetak() != null) {
                throw so.getIzuzetak();
            }
            
            Korisnik ulog = (Korisnik) so.getOdgovor();
            
            if (ulog != null && ulog.getKorisnikID() != -1) {
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("forme/Bundle_sr_RS").getString("USPESNO REGISTROVANJE"));
                KlijentskaForma kf = new KlijentskaForma(ulog, bundle_sr_RS);
                kf.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("forme/Bundle_sr_RS").getString("NEUSPESNO REGISTROVANJE"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void postaviSliku() {
        String jezik;
        
        if (bundle_sr_RS == ResourceBundle.getBundle("forme/Bundle_en")) {
            jezik = "/slike/srb.png";
        } else {
            jezik = "/slike/eng.png";
        }
        
        ImageIcon ikona = new javax.swing.ImageIcon(getClass().getResource(jezik));
        Image img = ikona.getImage() ;  
        Image newimg = img.getScaledInstance(lblJezik.getWidth(), lblJezik.getHeight(), Image.SCALE_SMOOTH);
        ikona = new ImageIcon( newimg );
        lblJezik.setIcon(ikona);
    }
