    public void stornirajNarudzbinu(ServerskiOdgovor so) {
        try {
            if (so.getIzuzetak() != null) {
                throw so.getIzuzetak();
            }
            
            Narudzbina n = (Narudzbina) so.getOdgovor();
            if (n != null) {
                JOptionPane.showMessageDialog(this, bundle_sr_RS.getString("USPESNO STORNIRANA"));
            } else {
                JOptionPane.showMessageDialog(this, bundle_sr_RS.getString("NEUSPESNO STORNIRANA"));
            }
            
            
//            JOptionPane.showMessageDialog(this, "Narudzbina je stornirana");
            popuniTabelu();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, bundle_sr_RS.getString("NEUSPESNO STORNIRANA") + "\n" + e.getMessage());
        }
    }

    public void postaviNarudzbine(ServerskiOdgovor so) {
        try {
            if (so.getIzuzetak() != null) {
                throw so.getIzuzetak();
            }

            ArrayList<DomenskiObjekat> narudzbine = (ArrayList<DomenskiObjekat>) so.getOdgovor();
            ModelTabeleNarudzbina mt = (ModelTabeleNarudzbina) tabelaNar.getModel();
            mt.setLista(narudzbine);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, bundle_sr_RS.getString("DOSLO JE DO GRESKE") + "\n" + e.getMessage());
        }
    }

    private void postaviSliku() {
        String jezik;
        
        if (bundle_sr_RS == ResourceBundle.getBundle("forme/Bundle_en")) {
            jezik = "/slike/srb.png";
        } else {
            jezik = "/slike/eng.png";
        }
        
        ImageIcon ikona = new javax.swing.ImageIcon(getClass().getResource(jezik));
        Image img = ikona.getImage() ;  
        Image newimg = img.getScaledInstance(lblJezik.getWidth(), lblJezik.getHeight(), Image.SCALE_SMOOTH);
        ikona = new ImageIcon( newimg );
        lblJezik.setIcon(ikona);
    }
    
    
