    private void txtPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPretraziActionPerformed
        // TODO add your handling code here:
        String polje = txtPretrazi.getText();

        if (polje.isEmpty()) {
            try {
                popuniTabelu();
            } catch (Exception ex) {
                Logger.getLogger(NarudzbineForma.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            ModelTabeleNarudzbina mt = (ModelTabeleNarudzbina) tabelaNar.getModel();
            mt.pretrazi(polje);
        }

    }//GEN-LAST:event_txtPretraziActionPerformed

