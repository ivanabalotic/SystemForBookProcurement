    private void btnObradiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObradiActionPerformed
        // TODO add your handling code here:
        int red = tabelaNar.getSelectedRow();
        ModelTabeleNarudzbina mt = (ModelTabeleNarudzbina) tabelaNar.getModel();
        if (red != -1) {
            Narudzbina n = mt.vratiNaruzbinu(red);
            
            if (n.getStatus().equals("stornirano")) {
                JOptionPane.showMessageDialog(this, bundle_sr_RS.getString("OVA NARUDZBINA JE STORNIRANA"));
                return;
            }
            
            UnesiNarudzbinu un = new UnesiNarudzbinu(this, true, n, ulog, bundle_sr_RS);
            un.setVisible(true);
        }
    }//GEN-LAST:event_btnObradiActionPerformed

